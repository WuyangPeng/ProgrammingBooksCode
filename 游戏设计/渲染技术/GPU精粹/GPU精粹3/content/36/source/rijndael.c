#if (RIJN_ENDIAN == RIJN_ENDIAN_BIG)
static inline uint32_t pack(const uint8_t *b)
{
    return (((uint32_t)b[0]) << 24) |
	   (((uint32_t)b[1]) << 16) |
	   (((uint32_t)b[2]) <<  8) |
	   (((uint32_t)b[3]));
}

static inline void unpack(uint32_t v, uint8_t *b)
{
    b[0] = (uint8_t)(v >> 24);
    b[1] = (uint8_t)(v >> 16);
    b[2] = (uint8_t)(v >>  8);
    b[3] = (uint8_t)v;
}
#else
static inline uint32_t pack(const uint8_t *b)
{
    return (((uint32_t)b[3]) << 24) |
	   (((uint32_t)b[2]) << 16) |
	   (((uint32_t)b[1]) <<  8) |
	   (((uint32_t)b[0]));
}

static inline void unpack(uint32_t v, uint8_t *b)
{
    b[3] = (uint8_t)(v >> 24);
    b[2] = (uint8_t)(v >> 16);
    b[1] = (uint8_t)(v >>  8);
    b[0] = (uint8_t)v;
}
#endif

static inline uint32_t l_pack(const uint8_t *b)
{
    return *(const uint32_t *)b;
}

static inline void l_unpack(uint32_t v, uint8_t *b)
{
    *(uint32_t *)b = v;
}


