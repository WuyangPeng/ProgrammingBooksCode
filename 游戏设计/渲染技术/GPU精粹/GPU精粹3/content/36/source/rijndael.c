/**
 * expand cipher key to generate key schedule
 *	@param	kp	return crypt param, key schedule
 *	@param	nb	number of block(4 fixed)
 *	@param	nk	cipher key length(4, 6, 8)
 *	@param	key	cipher key
 */
void rijn_expand_key(RijnKeyParam *kp, int nb, int nk, const char *key)
{
    int		i;
    uint32_t	t;

    kp->num_key = nk;
    kp->num_block = nb;

    // calc Nr
    if (nk == 4) {
	kp->num_round = 10;
    } else if (nk == 6) {
	kp->num_round = 12;
    } else {
	kp->num_round = 14;
    }

    // expand key for encrypt
    for (i=0; i<nk; i++) {
	kp->enc_key[i] = pack((const uint8_t *)&key[i*4]);
    }
    for (i=nk; i<num_block*(kp->num_round+1); i++) {
	t = kp->enc_key[i-1];
	if ((i % nk) == 0) {
	    t = sub_word(rot_word(t)) ^ r_con[i/nk];
	} else if ((nk > 6) && ((i % nk) == 4)) {
	    t = sub_word(t);
	}
	kp->enc_key[i] = kp->enc_key[i-nk] ^ t;
    }

    // expand key for decrypt
    for (i=0; i<num_block*(kp->num_round+1); i++) {
	kp->dec_key[i] = kp->enc_key[i];
    }
    for (i=1; i<kp->num_round; i++) {
	inv_mix_columns(&kp->dec_key[i*num_block]);
    }
}

/**
 * encrypt
 *	@param	kp	crypt param, key schedule
 *	@param	buff	plain -> crypted data pointer
 */
void rijn_encrypt(const RijnKeyParam *kp, char *buff)
{
    uint32_t	state_buf[num_block];
    uint8_t	*state = (uint8_t *)state_buf;
    int		i;

    // input & initialize
    for (i=0; i<num_block; i++) {
	state_buf[i] = pack((uint8_t *)&buff[i*num_block]);
    }
    add_round_key(state_buf, &kp->enc_key[0]);

    // loop round
    for (i=1; i<kp->num_round; i++) {
	sub_bytes_shift_rows(state);
	mix_columns_add_round_key(state, &kp->enc_key[i * num_block]);
    }

    // final round
    sub_bytes_shift_rows(state);
    add_round_key(state_buf, &kp->enc_key[kp->num_round * num_block]);

    // output
    for (i=0; i<num_block; i++) {
	unpack(state_buf[i], (uint8_t *)&buff[i*num_block]);
    }
}

/**
 * decrypt
 *	@param	kp	crypt param, key schedule
 *	@param	buff	crypted -> plain data pointer
 */
void rijn_decrypt(const RijnKeyParam *kp, char *buff)
{
    uint32_t	state_buf[num_block];
    uint8_t	*state = (uint8_t *)state_buf;
    int		i;

    // input & initialize
    for (i=0; i<num_block; i++) {
	state_buf[i] = pack((uint8_t *)&buff[i*num_block]);
    }
    add_round_key(state_buf, &kp->dec_key[kp->num_round * num_block]);

    // loop round
    for (i=kp->num_round-1; i>0; i--) {
	inv_shift_rows_inv_sub_bytes(state);
	inv_mix_columns_add_round_key(state, &kp->dec_key[i * num_block]);
    }

    // final round
    inv_shift_rows_inv_sub_bytes(state);
    add_round_key(state_buf, &kp->dec_key[0]);

    // output
    for (i=0; i<num_block; i++) {
	unpack(state_buf[i], (uint8_t *)&buff[i*num_block]);
    }
}
