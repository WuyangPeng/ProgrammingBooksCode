//An example showing escape sequences 
//to write \ and " in formatted output.

#include <iostream>		//for cout
using namespace std;

int main ()
{
	cout << "\nHow are you liking "
		<< " \"C++ Programming Today 2nd Edition\" ? ";
	
	cout <<"\nThe \\n is an escape sequence for a newline.\n";

	return 0;
}

